func MapKeyConvUnderline(data map[string]interface{}) map[string]interface{} {
	dataConv := make(map[string]interface{}, 0)
	for key, item := range data {
		if key != "children" {
			if item != nil && item != "" {
				camel2Case := Camel2Case(key)
				dataConv[camel2Case] = item
			}

		}
	}
	return dataConv
}

// 驼峰式写法转为下划线写法
func Camel2Case(name string) string {
	buffer := NewBuffer()
	for i, r := range name {
		if unicode.IsUpper(r) {
			if i != 0 {
				buffer.Append('_')
			}
			buffer.Append(unicode.ToLower(r))
		} else {
			buffer.Append(r)
		}
	}
	return buffer.String()
}

